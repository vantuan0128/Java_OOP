public class Date{
    public int day,month,year;
    public Date(int x, int y, int z){
        this.day=x;
        this.month=y;
        this.year=z;
    }
    public int getDay(){
        return this.day;
    }
    public int getMonth(){
        return this.month;
    }
    public int getYear(){
        return this.year;
    }
}